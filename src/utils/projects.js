// Helper functions for backward compatibility

/**
 * Returns all subcategories from all main categories as a flat array
 */
export const getAllSubcategories = () => {
  return projects.flatMap((mainCat) => mainCat.subcategories);
};

const toSlug = (text = "") =>
  text
    .toString()
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

/**
 * Finds a project by subcategory slug and project id (which is now a slug)
 */
export const getProjectBySlugAndId = (subcategorySlug, projectId) => {
  const allSubcats = getAllSubcategories();
  const subcategory = allSubcats.find((subcat) => subcat.slug === subcategorySlug);
  if (!subcategory) return null;
  // Match by slugified title
  return subcategory.items.find((item) => toSlug(item.title) === projectId);
};

/**
 * Returns the main category object for a given subcategory slug
 */
export const getMainCategoryForSubcategory = (subcategorySlug) => {
  return projects.find((mainCat) =>
    mainCat.subcategories.some((subcat) => subcat.slug === subcategorySlug)
  );
};
